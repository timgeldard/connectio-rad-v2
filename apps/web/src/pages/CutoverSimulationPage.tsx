// ─── Helpers ──────────────────────────────────────────────────────────────────

function modeVariant(mode: CutoverSimulationMode): 'default' | 'secondary' | 'destructive' | 'outline' {
  switch (mode) {
    case 'simulate-retirement': return 'default'
    case 'simulate-redirect': return 'secondary'
    case 'observe': return 'outline'
    case 'off': return 'destructive'
  }
}

function modeLabel(mode: CutoverSimulationMode): string {
  switch (mode) {
    case 'simulate-retirement': return 'Simulating Retirement'
    case 'simulate-redirect': return 'Redirecting'
    case 'observe': return 'Observe'
    case 'off': return 'Off'
  }
}

function modeDescription(mode: CutoverSimulationMode): string {
  switch (mode) {
    case 'simulate-retirement':
      return 'Legacy system is disabled; all traffic routes to ConnectIO.'
    case 'simulate-redirect':
      return 'Deep links from legacy system are intercepted and redirected to ConnectIO.'
    case 'observe':
      return 'Both systems run in parallel. Teams validate ConnectIO against legacy outcomes.'
    case 'off':
      return 'Simulation not yet started.'
  }
}

// ─── Sub-views ────────────────────────────────────────────────────────────────

function SimulationStatusKpi() {
  const counts = {
    retirement: SIMULATION_PAIRS.filter(p => p.currentMode === 'simulate-retirement').length,
    redirect: SIMULATION_PAIRS.filter(p => p.currentMode === 'simulate-redirect').length,
    observe: SIMULATION_PAIRS.filter(p => p.currentMode === 'observe').length,
    off: SIMULATION_PAIRS.filter(p => p.currentMode === 'off').length,
  }
  return (
    <div style={{ display: 'flex', gap: 12, marginBottom: 24, flexWrap: 'wrap' }}>
      {[
        { label: 'Retired', value: counts.retirement, variant: 'default' as const },
        { label: 'Redirecting', value: counts.redirect, variant: 'secondary' as const },
        { label: 'Observing', value: counts.observe, variant: 'outline' as const },
        { label: 'Not Started', value: counts.off, variant: 'destructive' as const },
      ].map(({ label, value }) => (
        <Card key={label} style={{ minWidth: 120 }}>
          <CardContent style={{ padding: '12px 16px' }}>
            <div style={{ fontSize: 11, color: 'var(--shell-fg-3)', marginBottom: 4, textTransform: 'uppercase', letterSpacing: '0.06em' }}>
              {label}
            </div>
            <span style={{ fontSize: 28, fontWeight: 700 }}>{value}</span>
          </CardContent>
        </Card>
      ))}
    </div>
  )
}

