function ConfigurationView() {
  const [selectedMode, setSelectedMode] = useState<CutoverSimulationMode | 'all'>('all')

  const modes: Array<{ value: CutoverSimulationMode | 'all'; label: string }> = [
    { value: 'all', label: 'All' },
    { value: 'simulate-retirement', label: 'Retired' },
    { value: 'simulate-redirect', label: 'Redirecting' },
    { value: 'observe', label: 'Observe' },
    { value: 'off', label: 'Not Started' },
  ]

  const filtered = selectedMode === 'all' ? SIMULATION_PAIRS : SIMULATION_PAIRS.filter(p => p.currentMode === selectedMode)

  return (
    <div>
      <div style={{ display: 'flex', gap: 8, marginBottom: 16, flexWrap: 'wrap' }}>
        {modes.map(m => (
          <Button
            key={m.value}
            variant={selectedMode === m.value ? 'default' : 'outline'}
            size="sm"
            onClick={() => setSelectedMode(m.value)}
          >
            {m.label}
          </Button>
        ))}
      </div>
      <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>
        {filtered.map(pair => <PairCard key={pair.pairId} pair={pair} />)}
      </div>
    </div>
  )
}

